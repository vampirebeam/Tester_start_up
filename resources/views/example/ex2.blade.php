ดังนั้น เวลาที่น้อยที่สุดในการข้ามสะพานคือ 2 + 8 + 2 + 1 + 2 = 15 นาที<br>

<?php
class BridgeCrossing {
    // คำนวณเวลาที่ใช้ในการข้ามสะพาน
    public static function minTime($times) {
        sort($times); // เรียงลำดับเวลาของคนในทีม
        $numPeople = count($times);
        $totalTime = 0;
        $steps = []; // เก็บขั้นตอนการข้ามสะพาน

        while ($numPeople > 4) {
            // ส่งคนที่ใช้เวลามากที่สุดกับคนที่ใช้เวลาน้อยที่สุดข้ามสะพาน
            $step = "ส่งคนที่ใช้เวลา {$times[0]} กับ {$times[1]} ข้ามสะพานด้วยตะเกียง";
            $totalTime += $times[1];
            $steps[] = $step;
            $numPeople -= 2; // ลดจำนวนคนที่เหลือลง 2 คน
        }

        if ($numPeople == 4) {
            // ส่งคนที่ใช้เวลามากที่สุดกับคนที่ใช้เวลาน้อยที่สุดข้ามสะพาน
            $step = "ส่งคนที่ใช้เวลา {$times[0]} กับ {$times[1]} ข้ามสะพานด้วยตะเกียง";
            $totalTime += $times[1];
            $steps[] = $step;
        }

        // ข้ามคนสุดท้าย
        $step = "ส่งคนที่ใช้เวลา {$times[0]} ข้ามสะพานด้วยตะเกียง";
        $totalTime += $times[0];
        $steps[] = $step;

        // แสดงผลลัพธ์พร้อมขั้นตอนการข้ามสะพาน
        echo "เวลาที่น้อยที่สุดในการข้ามสะพานคือ: {$totalTime} นาที\n";
        echo "ขั้นตอนการข้ามสะพาน:\n";
        foreach ($steps as $step) {
            echo "{$step}\n";
        }
    }
}

// ตัวอย่างการใช้งาน
$times = [1, 2, 5, 8];
BridgeCrossing::minTime($times);
?>

